package com.bean;

import java.util.Date;

public class Spend{
	
	int sid;
	
	int uid;
	
	float smoney;
	
	Date sdate;
	
	int oid;

	public int getSid() {
		return sid;
	}

	public void setSid(int sid) {
		this.sid = sid;
	}

	public int getUid() {
		return uid;
	}

	public void setUid(int uid) {
		this.uid = uid;
	}

	public float getSmoney() {
		return smoney;
	}

	public void setSmoney(float smoney) {
		this.smoney = smoney;
	}

	public Date getSdate() {
		return sdate;
	}

	public void setSdate(Date sdate) {
		this.sdate = sdate;
	}

	public int getOid() {
		return oid;
	}

	public void setOid(int oid) {
		this.oid = oid;
	}

	
	
}
